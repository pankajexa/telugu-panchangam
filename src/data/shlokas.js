/**
 * Get the shloka of the day.
 * - On festival days: prefer festival-tagged Ramayana shloka matching the festival
 * - On non-festival days: rotate through Durga Sapta Shloki (7 shlokas)
 * @param {Date} date
 * @param {string|null} festivalEnglish - English name of today's festival (if any)
 */
export function getShlokaOfTheDay(date, festivalEnglish = null) {
  if (festivalEnglish) {
    const key = festivalEnglish.toLowerCase();
    const match = SHLOKAS.find(s =>
      s.festivalTags?.some(tag => key.includes(tag.replace(/_/g, ' ')))
    );
    if (match) return match;
  }
  // Non-festival days: rotate through Durga Sapta Shloki
  const start = new Date(date.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((date - start) / 86400000);
  return DURGA_SAPTA_SHLOKI[dayOfYear % DURGA_SAPTA_SHLOKI.length];
}

/**
 * Get all unique purpose categories with their shlokas.
 */
export function getShlokasByCategory() {
  const map = {};
  for (const s of SHLOKAS) {
    if (!map[s.purposeCategory]) {
      map[s.purposeCategory] = { category: s.purposeCategory, label: s.purpose, shlokas: [] };
    }
    map[s.purposeCategory].shlokas.push(s);
  }
  return Object.values(map);
}

